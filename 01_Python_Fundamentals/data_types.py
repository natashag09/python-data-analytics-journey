# Topic: Data Types 

# String 
name = "Natasha"
print(name)
print(type(name))

# Integer
age = 22
print(age)
print(type(age))

# Float
height = 5.4
print(height)
print(type(height))

# Boolean
is_learning_python = True
print(is_learning_python)
print(type(is_learning_python))

# Complex
complex_number = 2 + 3j
print(complex_number)
print(type(complex_number))

# List
fruits = ["Apple", "Banana", "Mango"]
print(fruits)
print(type(fruits))

# Tuple
colors = ("Red", "Blue", "Green")
print(colors)
print(type(colors))

# Set
numbers = {1, 2, 3, 4, 5}
print(numbers)
print(type(numbers))

# Dictionary
student = {
    "name": "Natasha",
    "age": 22,
    "course": "Data Analytics"
}

print(student)
print(type(student))

# Practice Variables

city = "Muzaffarnagar"
temperature = 35.5
population = 1500000
is_raining = False

print(city, type(city))
print(temperature, type(temperature))
print(population, type(population))
print(is_raining, type(is_raining))