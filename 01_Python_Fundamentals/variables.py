# Topic: Variables in Python 

# String Variable
name = "Natasha"

# Integer Variable
age = 22

# Float Variable
height = 5.4

# Boolean Variable
is_learning_python = True

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Learning Python:", is_learning_python)

print("\n--- Multiple Assignment ---")

# Multiple Assignment
x, y, z = 10, 20, 30

print("x =", x)
print("y =", y)
print("z =", z)

print("\n--- Same Value Assignment ---")

# Assigning the same value to multiple variables
a = b = c = 100

print("a =", a)
print("b =", b)
print("c =", c)

print("\n--- Variable Reassignment ---")

city = "Delhi"
print("Original City:", city)

city = "Mumbai"
print("Updated City:", city)

print("\n--- Constant Naming Convention ---")

PI = 3.14159

print("PI =", PI)
